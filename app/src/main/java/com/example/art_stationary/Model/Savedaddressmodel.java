package com.example.art_stationary.Model;

public class Savedaddressmodel {
    private String ship_street;
    private String ship_building;
    private String ship_appartment;
    private String ship_block;
    private String ship_avenue;
    private String ship_floor;
    private String id;


    public Savedaddressmodel() {

    }

    public String getShip_street() {
        return ship_street;
    }

    public void setShip_street(String ship_street) {
        this.ship_street = ship_street;
    }

    public String getShip_building() {
        return ship_building;
    }

    public void setShip_building(String ship_building) {
        this.ship_building = ship_building;
    }

    public String getShip_appartment() {
        return ship_appartment;
    }

    public void setShip_appartment(String ship_appartment) {
        this.ship_appartment = ship_appartment;
    }

    public String getShip_block() {
        return ship_block;
    }

    public void setShip_block(String ship_block) {
        this.ship_block = ship_block;
    }

    public String getShip_avenue() {
        return ship_avenue;
    }

    public void setShip_avenue(String ship_avenue) {
        this.ship_avenue = ship_avenue;
    }

    public String getShip_floor() {
        return ship_floor;
    }

    public void setShip_floor(String ship_floor) {
        this.ship_floor = ship_floor;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
